  <h3>Functionalities</h3>
  <p>The radio buttons are responsible for when how password will be shown. There are 2 options.</p>
  <ol>
    <li>Show password when on hover (mouse over)</li>
    <li>Show password all the time</li>
  </ol>
  <p>The checkboxes indicates extra functionalities.</p>
  <ol>
    <li>
      <strong>Auto Mode</strong> will trigger the extension automatically. i.e. Password can be viewed
      when page is loaded. If this is not checked, you have to open the extension by clicking or a keyboard
      shortcut.
    </li>
    <li>
      <strong>Copy On Double Click</strong> allows user to double copy password to clipboard by double
      clicking the password field. A notification will be sent if successful.
    </li>
  </ol>
  <h3>Keyboard Shortcuts</h3>
  <p>This extension provides 2 keyboard extensions</p>
  <ol>
    <li><code>Ctrl+Shift+L</code>: Tigger the extension popup</li>
    <li><code>Ctrl+Shift+S</code>: Toggle password on web page without opening extension popup</li>
  </ol>
  <p>The keyboard shortcuts should work on Linux, MacOS, Windows, ChromeOS.</p>
  <p>On MacOS, replace the <code>Ctrl</code> key with <code>Command</code> key.</p>
  <p>Go to <code>chrome://extensions</code> to change the default keyboard shortcuts.</p>
